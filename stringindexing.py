# indexing = ACCESSING ELEMENTS OF A STRING USING [] (INDEXING OPERATOR)
#   [START : END : STOP ]

credit_number = "1234-5678-9012-3456" 

print(credit_number[2])# it will print 3
print(credit_number[:4])# it will print 1234 
print(credit_number[-1])#it will print 6 
print(credit_number[::2 ])#it will print 13-6891-46 
#PRINT LAST FOUR DIGIT OF CREDIT CARD NUMBER
print(credit_number[-4:])#it will print 3456 
print(credit_number)